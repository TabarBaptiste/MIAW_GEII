        <h2>Programme</h2>
        <p>Le BUT GEII forme des techniciens supérieurs dont les activités dépendent, pour une large part, du type d’entreprise où ils exercent leurs fonctions : elles sont spécialisées dans une grande entreprise, plus larges et variées dans une petite entreprise ou un laboratoire de recherche.</p>
        <p>La formation propose 2000 heures de formation encadrée et 600 heures de projets tuteurés. Le parcours de formation conduisant au BUT est constitué d’un tronc commun regroupant 3 compétences, et d’un parcours (débutant en deuxième année en parallèle avec la poursuite du tronc commun) permettant à l’étudiant de développer une compétence supplémentaire.</p>
        <p>Le BUT est une formation avec un Programme National garantissant une formation équivalente dans tous les départements GEII de France. En première année, les étudiants découvrent l’ensemble des fonctions et montages de bases dans les domaines du Génie Électrique (Électronique, Électricité, Électrotechnique, Énergie) et de l’Informatique Industrielle (Programmation, Architecture des ordinateurs, Réseaux, Automatisme) qui seront nécessaires lors de la conception, la vérification et la maintenance des systèmes. En deuxième et troisième années, les étudiants approfondissent ces trois compétences dans un tronc commun (environ 80% de l’enseignement) et choisissent un parcours (environ 20% de l’enseignement) portant sur une nouvelle compétence, parmi 3 choix :</p>
        <ul>
            <li>Automatisme et Informatique Industrielle (Intégration)</li>
            <li>Électricité et Maitrise de l’énergie (Installation)</li>
            <li>Électronique et informatique Industrielle (Production)</li>
        </ul>
        <p>En formation initiale, réalisation de deux stages, 8 à 10 semaines en BUT 2 et 14 à 16 semaines en BUT 3. L’apprentissage est possible sur deux ans (BUT 2 et BUT 3) ou sur un an (BUT 3).</p>
        <h3>Organisation de l’enseignement et choix pédagogiques</h3>
        <p>L’enseignement universitaire est organisé en Cours Magistraux (promotion entière), Travaux Dirigés (groupe de 24 étudiants) et Travaux Pratiques (groupe de 12 étudiants). Nous faisons le choix, en première année, de dispenser les enseignements pour moitié en groupes de TD et pour moitié en groupes de TP. Quelques cours magistraux sont introduits en deuxième et troisième année.</p>
        <p>Les 2000 heures de formation encadrée, complétées par les 600 heures de projets tuteurés et 22 à 26 semaines de stage, sont dispensées sur une durée de 6 semestres et représentent environ 33 heures de formation par semaine pour les étudiants.</p>
