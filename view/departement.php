        <h2>Présentation du BUT GEII</h2>
        <p>La formation dispensée dans les départements Génie Électrique et Informatique Industrielle (GEII) est construite par une communauté au sein de laquelle coopèrent enseignants et professionnels de l’industrie et des services. Elle constitue un dispositif national universitaire qui prépare en 3 ans l’étudiant à l’insertion professionnelle tout en lui laissant la possibilité de poursuivre ses études. Nous offrons un parcours en formation initial et la possibilité d’une formation en alternance en deuxième et troisième années ou seulement en troisième année.</p>
        <p>Plus d’informations <a href="https://but-geii.fr/le-but-quest-ce-que-cest/">ici</a></p>
        <p>Le département propose aussi deux licences professionnelles :</p>
